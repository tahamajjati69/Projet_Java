    private void forgotpasswrdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_forgotpasswrdActionPerformed
        // TODO add your handling code here:
        String email = jTextField1.getText();

    if (email.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Veuillez entrer votre email");
        return;
    }

    UtilisateurDao dao = new UtilisateurDao();

    if (!dao.emailExiste(email)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Email introuvable");
        return;
    }

    String code = dao.genererCode();
    dao.sauvegarderCode(email, code);

    EmailUtil.envoyerCode(email, code);

    String codeSaisi = javax.swing.JOptionPane.showInputDialog(
            this,
            "Un code a été envoyé à votre email.\nEntrez le code :"
    );

    if (codeSaisi == null) return;

    if  (dao.verifierCode(email, codeSaisi.trim())) {
 
        dao.clearCode(email);
        javax.swing.JOptionPane.showMessageDialog(this, "Connexion réussie !");
        new MainFrame().setVisible(true);
        this.dispose();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Code incorrect");
    }
    }//GEN-LAST:event_forgotpasswrdActionPerformed



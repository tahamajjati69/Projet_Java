    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String email = jTextField1.getText();
        String pass = new String(jPasswordField1.getPassword());

        // Vérification vide
        if (email.isEmpty() || pass.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs !");
            return;
        }

        // Vérification dans la base MySQL
        UtilisateurDao dao = new UtilisateurDao();
        boolean ok = dao.login(email, pass);

        if (ok) {
            javax.swing.JOptionPane.showMessageDialog(this, "Connexion réussie !");
            new MainFrame().setVisible(true);
            this.dispose();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Email ou mot de passe incorrect !");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


    private void adtxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adtxtActionPerformed
        // TODO double poids = Double.parseDouble(poitxt.getText());
    double poids = Double.parseDouble(poitxt.getText()); // lire le poids
    String ville = viltxt.getText();                     // lire la ville

    Colis c = new Colis(poids, ville);                  // créer le colis
    fs.ajouterColis(c);                                 // ajouter à la base

    loadColis();                                        // recharger le tableau
    JOptionPane.showMessageDialog(this, "Colis ajouté !");
    }//GEN-LAST:event_adtxtActionPerformed

    private void uptxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uptxtActionPerformed
        // TODO add your handling code here:
         int row = Coliis.getSelectedRow();
    if (row >= 0) {
        int reference = (int) model.getValueAt(row, 0);
        double poids = Double.parseDouble(poitxt.getText());
        String ville = viltxt.getText();

        Colis c = new Colis(reference, poids, ville);
        fs.modifierColis(c); // mettre à jour dans la base
        loadColis(); // recharger le tableau
        JOptionPane.showMessageDialog(this, "Colis modifié !");
    } else {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un colis !");
    }
    }//GEN-LAST:event_uptxtActionPerformed

    private void suptxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suptxtActionPerformed
        // TODO add your handling code here:
        int row = Coliis.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un colis à supprimer !");
        return;
    }

    // Affiche un message Oui/Non
    int confirm = JOptionPane.showConfirmDialog(this, 
        "Voulez-vous vraiment supprimer ce colis ?", 
        "Confirmation de suppression", 
        JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        int reference = (int) model.getValueAt(row, 0);
        fs.supprimerColis(reference);  // méthode à créer dans ColisService
        loadColis();                   // recharge le tableau
        JOptionPane.showMessageDialog(this, "Colis supprimé !");
    }
    }//GEN-LAST:event_suptxtActionPerformed



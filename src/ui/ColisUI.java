  private void loadColis() {
    model.setRowCount(0); // vider le tableau
    for (Colis c : fs.listeColis()) {
        model.addRow(new Object[]{
            c.getReference(),
            c.getPoids(),
            c.getVilleDestination()
        });
    }
}
